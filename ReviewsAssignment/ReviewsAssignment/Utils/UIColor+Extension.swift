import Foundation
import UIKit

extension UIColor {
    class func NavibationBarBlue() -> UIColor {
        return UIColor(red: 57/255.0, green: 158/255.0, blue: 241/255.0, alpha: 1.0)
    }
    
    class func warmGrey() -> UIColor {
        return UIColor(white: 140.0 / 255.0, alpha: 1.0)
    }
    
    class func azure() -> UIColor {
        return UIColor(red: 3.0 / 255.0, green: 155.0 / 255.0, blue: 239.0 / 255.0, alpha: 1.0)
    }
    
    class func darkGreyCustom() -> UIColor {
        return UIColor(red: 40.0 / 255.0, green: 40.0 / 255.0, blue: 41.0 / 255.0, alpha: 1.0)
    }
}
